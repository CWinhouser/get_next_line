int	ft_atoi(const char *str)
{
	int i;
	int cnt;

	i = 0;
	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (cnt > 0)
		{
			i = (i * 10) + (str[cnt] - 48);
		}
		else
			i = (i + str[cnt]) - 48;
		cnt++;
	}
	return (i);
}
