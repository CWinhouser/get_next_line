#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, int n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str1[i] == str2[i] && str1[i] != '\0' && str2[i] != '\0' && i <= n)
	{
		i++;
		j = str1[i] - str2[i];
	}
	return (j);
}
