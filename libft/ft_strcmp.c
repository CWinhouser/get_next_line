#include "libft.h"

int	ft_strcmp(const char *str1, const char *str2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str1[i] == str2[i] && str1[i] != '\0' && str2[i] != '\0')
	{
		i++;
		j = str1[i] - str2[i];
	}
	return (j);
}
